    private void loadTable() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) table.getModel();
            dtm.setRowCount(0);
            ResultSet rs = DB.search("select * from basic_salary");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("emp_id"));
                v.add(rs.getString("basic_salary"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            log.error("Load Basic salary Details Failed", e);
        }
    }
