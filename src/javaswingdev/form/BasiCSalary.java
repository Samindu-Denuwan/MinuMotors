    private void save1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save1ActionPerformed
        try {
            ResultSet rs = DB.search("select * from basic_salary where emp_id ='" + txt_emp_id.getText() + "' ");
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Salary is already Added.Please use UPDATE button to update details", "Error", JOptionPane.ERROR_MESSAGE);

            } else {
                if (!txt_emp_id.getText().equals("") && !txt_salary.getText().equals("")) {

                    String stat = "none";

                    String status = null;
                    if (switchButton.isSelected()) {
                        status = "1";
                    } else {
                        status = "0";
                    }

                    DB.push("insert into basic_salary values('"
                            + txt_emp_id.getText() + "','"
                            + txt_salary.getText() + "','"
                            + stat + "','"
                            + status + "')");

                    DB.push("insert into system_activity (detail, date, time, employee) values ("
                            + "'Basic salary Saved',CURRENT_TIMESTAMP(),CURRENT_TIMESTAMP(),'" + SystemConfigData.getEmployee() + "')");

                    log.info("Basic Salary Saved Successful. Employee : " + txt_emp_id.getText() + " | By : " + SystemConfigData.getEmployee());
                    clearFields();
                    JOptionPane.showMessageDialog(this, "Basic Salary Added!!", "Saved", JOptionPane.INFORMATION_MESSAGE);
                    loadTable();

                } else {
                    JOptionPane.showMessageDialog(this, "Please Enter Salary!", "Error", JOptionPane.ERROR_MESSAGE);

                }
            }
        } catch (Exception e) {
            log.error("Salary Save Failed", e);
        }
    }//GEN-LAST:event_save1ActionPerformed

