    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {

            if (!txt_emp_id.getText().equals("") && !txt_salary.getText().equals("")) {
                ResultSet rs = DB.search("select * from basic_salary where emp_id ='" + txt_emp_id.getText() + "' ");
                if (rs.next()) {

                    String stat = "none";

                    String status = null;
                    if (switchButton.isSelected()) {
                        status = "1";
                    } else {
                        status = "0";
                    }

                    DB.push("update basic_salary set  basic_salary='" + txt_salary.getText() + "'"
                            + ", stat='" + stat + "',status='" + status + "' where emp_id ='"
                            + txt_emp_id.getText() + "' ");

                    DB.push("insert into system_activity (detail, date, time, employee) values ("
                            + "'Basic salary Update',CURRENT_TIMESTAMP(),CURRENT_TIMESTAMP(),'" + SystemConfigData.getEmployee() + "')");

                    log.info("Basic Salary Update Successful. Employee : " + txt_emp_id.getText() + " | By : " + SystemConfigData.getEmployee());
                    clearFields();
                    JOptionPane.showMessageDialog(this, "Basic Salary Updated!!", "Saved", JOptionPane.INFORMATION_MESSAGE);
                    loadTable();

                } else {
                    JOptionPane.showMessageDialog(this, "Please use SAVE button to new Employee", "Error", JOptionPane.ERROR_MESSAGE);

                }
            } else {
                JOptionPane.showMessageDialog(this, "Please Enter Salary!", "Error", JOptionPane.ERROR_MESSAGE);

            }
        } catch (Exception e) {
            log.error("Salary Update Failed", e);
        }
    }//GEN-LAST:event_updateActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        try {
            DefaultTableModel dtm = (DefaultTableModel) table.getModel();
            int selectRow = table.getSelectedRow();

            String emp_id = dtm.getValueAt(selectRow, 0).toString();
            txt_emp_id.setText(emp_id);
            String salary = dtm.getValueAt(selectRow, 1).toString();
            txt_salary.setText(salary);
            
             ResultSet rs3 = DB.search("select * from basic_salary where emp_id = '" + emp_id + "'");
            if (rs3.next()) {
                String status = (rs3.getString("status"));
                 if (status.equals("1")) {
                    switchButton.setSelected(true);
                } else {
                    switchButton.setSelected(false);
                }
                

            }

            ResultSet rs = DB.search("select * from employee where emp_id = '" + emp_id + "'");
            if (rs.next()) {
                txt_emp_name.setText(rs.getString("fname") + " " + rs.getString("lname"));

            }
            String roleID = (rs.getString("role_id"));
            ResultSet rs2 = DB.search("select * from role where role_id = '" + roleID + "'");
            if (rs2.next()) {
                String role = (rs2.getString("role"));
                txt_role.setText(role);

            }

        } catch (Exception e) {
            log.error("Load Employee Details Failed", e);
        }
    }//GEN-LAST:event_tableMouseClicked

